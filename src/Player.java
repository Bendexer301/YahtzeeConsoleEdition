/**
 * Player class that represents a player in the game.
 */
public class Player {

    private String name;

    /**
     * Constructor for the Player class.
     * @param name
     */
    public Player(String name) {
        this.name = name;
    }

    /**
     * Returns the name of the player.
     * @return
     */
    public String getName() {
        return name;
    }
}
